package com.hybrid.gungduk.dto;

public class LotateDto {
	Double latitude;
	Double logitude;
	Double minLati;
	Double maxLati;
	Double minLogi;
	Double maxLogi;
	
	public Double getLatitude() {
		return latitude;
	}
	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}
	public Double getLogitude() {
		return logitude;
	}
	public void setLogitude(Double logitude) {
		this.logitude = logitude;
	}
	public Double getMinLati() {
		return minLati;
	}
	public void setMinLati(Double minLati) {
		this.minLati = minLati;
	}
	public Double getMaxLati() {
		return maxLati;
	}
	public void setMaxLati(Double maxLati) {
		this.maxLati = maxLati;
	}
	public Double getMinLogi() {
		return minLogi;
	}
	public void setMinLogi(Double minLogi) {
		this.minLogi = minLogi;
	}
	public Double getMaxLogi() {
		return maxLogi;
	}
	public void setMaxLogi(Double maxLogi) {
		this.maxLogi = maxLogi;
	}
	
}
